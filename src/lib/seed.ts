const hoursAgo = (h: number) => new Date(now - h * 36e5).toISOString();

function make(
  partial: Omit<Article, "id" | "slug" | "createdAt">,
): Article {
  const id = hashId(partial.headline + partial.sourceUrl);
  return {
    ...partial,
    id,
    slug: slugify(partial.headline, id),
    createdAt: new Date().toISOString(),
  };
}

